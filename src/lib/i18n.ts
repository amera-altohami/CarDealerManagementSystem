export type MessageKey =
  | keyof typeof messages.en
  | keyof typeof legacyMessages.en

export function translate(locale: Locale, key: MessageKey) {
  const currentMessages = messages[locale] as Partial<
    Record<MessageKey, string>
  >
  const fallbackMessages = legacyMessages[locale] as Partial<
    Record<MessageKey, string>
  >

  return currentMessages[key] ?? fallbackMessages[key] ?? String(key)
}

