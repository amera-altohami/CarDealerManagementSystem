const src = fs.readFileSync(filePath, 'utf8')
const start = src.indexOf('const messages = ')
const end = src.indexOf('export type MessageKey')

if (start === -1 || end === -1) {
  throw new Error('Could not locate messages object')
}

const raw = src.slice(start + 'const messages = '.length, end).trim()
const messages = eval(`(${raw.replace(/ as const$/, '')})`)
const repairString = (value) =>
  /[ØÙÃÂï¿½]/.test(value) ? Buffer.from(value, 'latin1').toString('utf8') : value

const repairedAr = Object.fromEntries(
  Object.entries(messages.ar).map(([key, value]) => [key, repairString(value)])
)

const mergedAr = { ...repairedAr, ...patches }

const before = src.slice(0, start + 'const messages = '.length)
const after = src.slice(end)
const enEntries = Object.entries(messages.en)
  .map(([key, value]) => `${key}:\`${String(value).replaceAll('`', '\\`')}\``)
  .join(',')
const arEntries = Object.entries(mergedAr)
  .map(([key, value]) => `${key}:\`${String(value).replaceAll('`', '\\`')}\``)
  .join(',')

const next = `${before}{en:{${enEntries}},ar:{${arEntries}}} as const\n\n${after}`
fs.writeFileSync(filePath, next, 'utf8')
